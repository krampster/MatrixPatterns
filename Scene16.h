class Scene16 : public Pattern {
public:
  Scene16(CRGB* ledsArray, int numLeds)
  {
    leds = ledsArray;
    numLeds = numLeds;
  }
  void initialize() override
  {
  }

  void drawBitmap()
  {
    // Loop through each LED and set it to the correct pixel from the bitmap.
    // LEDs start at bottom left, and pixels start at top right.
    for (uint i = 0; i < 256; i++) {
      leds[Utils::XY(i % kMatrixWidth, i / kMatrixWidth)] = CRGB(image_data_Scene[255 - i]);
    }

    FastLED.show();
  }

  void update() override
  {
    drawBitmap();
    delay(20);
  }

private:
  CRGB* leds;
  int numLeds;
};
